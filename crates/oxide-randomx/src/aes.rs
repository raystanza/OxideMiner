#[cfg(test)]
mod tests {
    use super::{
        aes_round_decrypt, aes_round_decrypt_soft, aes_round_encrypt, aes_round_encrypt_soft,
    };
    use crate::flags::RandomXFlags;

    #[test]
    fn aes_round_public_matches_soft() {
        let key = [
            0x53, 0xa5, 0xac, 0x6d, 0x09, 0x66, 0x71, 0x62, 0x2b, 0x55, 0xb5, 0xdb, 0x17, 0x49,
            0xf4, 0xb4,
        ];
        let mut block_pub = [0x00u8; 16];
        let mut block_soft = [0x00u8; 16];
        let flags = RandomXFlags {
            aes_ni: false,
            soft_aes: true,
            ..Default::default()
        };
        aes_round_encrypt(&mut block_pub, &key, &flags);
        aes_round_encrypt_soft(&mut block_soft, &key);
        assert_eq!(block_pub, block_soft);

        aes_round_decrypt(&mut block_pub, &key, &flags);
        aes_round_decrypt_soft(&mut block_soft, &key);
        assert_eq!(block_pub, block_soft);
    }

    #[test]
    fn aes_round_soft_matches_aesni() {
        #[cfg(target_arch = "x86_64")]
        {
            if !std::is_x86_feature_detected!("aes") {
                return;
            }
            let key = [0x21u8; 16];
            let mut block_soft = [0x3au8; 16];
            let mut block_hw = [0x3au8; 16];
            let flags = RandomXFlags {
                aes_ni: true,
                soft_aes: false,
                ..Default::default()
            };
            aes_round_encrypt_soft(&mut block_soft, &key);
            aes_round_encrypt(&mut block_hw, &key, &flags);
            assert_eq!(block_soft, block_hw);

            aes_round_decrypt_soft(&mut block_soft, &key);
            aes_round_decrypt(&mut block_hw, &key, &flags);
            assert_eq!(block_soft, block_hw);
        }
    }
}
