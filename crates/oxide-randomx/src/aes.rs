//! AES round helpers (hardware-accelerated when available).

use crate::flags::RandomXFlags;

/// Apply a single AES encryption round to `block` with `round_key`.
pub fn aes_round_encrypt(block: &mut [u8; 16], round_key: &[u8; 16], flags: &RandomXFlags) {
    if flags.aes_ni && !flags.soft_aes && aes_ni_available() {
        #[cfg(target_arch = "x86_64")]
        unsafe {
            return aes_round_encrypt_aesni(block, round_key);
        }
    }
    aes_round_encrypt_soft(block, round_key);
}

/// Apply a single AES decryption round to `block` with `round_key`.
pub fn aes_round_decrypt(block: &mut [u8; 16], round_key: &[u8; 16], flags: &RandomXFlags) {
    if flags.aes_ni && !flags.soft_aes && aes_ni_available() {
        #[cfg(target_arch = "x86_64")]
        unsafe {
            return aes_round_decrypt_aesni(block, round_key);
        }
    }
    aes_round_decrypt_soft(block, round_key);
}

fn aes_round_encrypt_soft(block: &mut [u8; 16], round_key: &[u8; 16]) {
    sub_bytes(block);
    shift_rows(block);
    mix_columns(block);
    add_round_key(block, round_key);
}

fn aes_round_decrypt_soft(block: &mut [u8; 16], round_key: &[u8; 16]) {
    inv_shift_rows(block);
    inv_sub_bytes(block);
    inv_mix_columns(block);
    add_round_key(block, round_key);
}

fn add_round_key(block: &mut [u8; 16], round_key: &[u8; 16]) {
    for (b, k) in block.iter_mut().zip(round_key.iter()) {
        *b ^= *k;
    }
}

fn sub_bytes(block: &mut [u8; 16]) {
    for b in block.iter_mut() {
        *b = S_BOX[*b as usize];
    }
}

fn inv_sub_bytes(block: &mut [u8; 16]) {
    for b in block.iter_mut() {
        *b = INV_S_BOX[*b as usize];
    }
}

fn shift_rows(block: &mut [u8; 16]) {
    // Row 1 shift by 1
    let t = block[1];
    block[1] = block[5];
    block[5] = block[9];
    block[9] = block[13];
    block[13] = t;

    // Row 2 shift by 2
    let t0 = block[2];
    let t1 = block[6];
    block[2] = block[10];
    block[6] = block[14];
    block[10] = t0;
    block[14] = t1;

    // Row 3 shift by 3
    let t = block[15];
    block[15] = block[11];
    block[11] = block[7];
    block[7] = block[3];
    block[3] = t;
}

fn inv_shift_rows(block: &mut [u8; 16]) {
    // Row 1 shift by 1 right
    let t = block[13];
    block[13] = block[9];
    block[9] = block[5];
    block[5] = block[1];
    block[1] = t;

    // Row 2 shift by 2 right
    let t0 = block[2];
    let t1 = block[6];
    block[2] = block[10];
    block[6] = block[14];
    block[10] = t0;
    block[14] = t1;

    // Row 3 shift by 3 right
    let t = block[3];
    block[3] = block[7];
    block[7] = block[11];
    block[11] = block[15];
    block[15] = t;
}

fn mix_columns(block: &mut [u8; 16]) {
    for col in 0..4 {
        let i = col * 4;
        let a0 = block[i];
        let a1 = block[i + 1];
        let a2 = block[i + 2];
        let a3 = block[i + 3];
        block[i] = mul2(a0) ^ mul3(a1) ^ a2 ^ a3;
        block[i + 1] = a0 ^ mul2(a1) ^ mul3(a2) ^ a3;
        block[i + 2] = a0 ^ a1 ^ mul2(a2) ^ mul3(a3);
        block[i + 3] = mul3(a0) ^ a1 ^ a2 ^ mul2(a3);
    }
}

fn inv_mix_columns(block: &mut [u8; 16]) {
    for col in 0..4 {
        let i = col * 4;
        let a0 = block[i];
        let a1 = block[i + 1];
        let a2 = block[i + 2];
        let a3 = block[i + 3];
        block[i] = mul14(a0) ^ mul11(a1) ^ mul13(a2) ^ mul9(a3);
        block[i + 1] = mul9(a0) ^ mul14(a1) ^ mul11(a2) ^ mul13(a3);
        block[i + 2] = mul13(a0) ^ mul9(a1) ^ mul14(a2) ^ mul11(a3);
        block[i + 3] = mul11(a0) ^ mul13(a1) ^ mul9(a2) ^ mul14(a3);
    }
}

fn mul2(x: u8) -> u8 {
    let xt = x << 1;
    if x & 0x80 != 0 {
        xt ^ 0x1b
    } else {
        xt
    }
}

fn mul3(x: u8) -> u8 {
    mul2(x) ^ x
}

fn mul9(x: u8) -> u8 {
    let x2 = mul2(x);
    let x4 = mul2(x2);
    let x8 = mul2(x4);
    x8 ^ x
}

fn mul11(x: u8) -> u8 {
    let x2 = mul2(x);
    let x4 = mul2(x2);
    let x8 = mul2(x4);
    x8 ^ x2 ^ x
}

fn mul13(x: u8) -> u8 {
    let x2 = mul2(x);
    let x4 = mul2(x2);
    let x8 = mul2(x4);
    x8 ^ x4 ^ x
}

fn mul14(x: u8) -> u8 {
    let x2 = mul2(x);
    let x4 = mul2(x2);
    let x8 = mul2(x4);
    x8 ^ x4 ^ x2
}

#[cfg(target_arch = "x86_64")]
#[target_feature(enable = "aes")]
unsafe fn aes_round_encrypt_aesni(block: &mut [u8; 16], round_key: &[u8; 16]) {
    use core::arch::x86_64::{__m128i, _mm_aesenc_si128, _mm_loadu_si128, _mm_storeu_si128};
    let state = unsafe { _mm_loadu_si128(block.as_ptr() as *const __m128i) };
    let key = unsafe { _mm_loadu_si128(round_key.as_ptr() as *const __m128i) };
    let out = _mm_aesenc_si128(state, key);
    unsafe { _mm_storeu_si128(block.as_mut_ptr() as *mut __m128i, out) };
}

#[cfg(target_arch = "x86_64")]
#[target_feature(enable = "aes")]
unsafe fn aes_round_decrypt_aesni(block: &mut [u8; 16], round_key: &[u8; 16]) {
    use core::arch::x86_64::{__m128i, _mm_aesdec_si128, _mm_loadu_si128, _mm_storeu_si128};
    let state = unsafe { _mm_loadu_si128(block.as_ptr() as *const __m128i) };
    let key = unsafe { _mm_loadu_si128(round_key.as_ptr() as *const __m128i) };
    let out = _mm_aesdec_si128(state, key);
    unsafe { _mm_storeu_si128(block.as_mut_ptr() as *mut __m128i, out) };
}

fn aes_ni_available() -> bool {
    #[cfg(target_arch = "x86_64")]
    {
        std::is_x86_feature_detected!("aes")
    }
    #[cfg(not(target_arch = "x86_64"))]
    {
        false
    }
}

